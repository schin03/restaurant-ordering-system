/**
 * Scans public/menu-photos for primary numbered JPGs only (e.g. 124.jpg, not 124_1.jpg).
 * Emits src/data/menuPhotoManifest.js with MENU_PHOTO_BY_KEY: `${sectionId}:${num}` -> URL path.
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.join(__dirname, '..')
const photosRoot = path.join(root, 'public', 'menu-photos')
const outFile = path.join(root, 'src', 'data', 'menuPhotoManifest.js')

/** On-disk folder name -> MENU_SECTIONS id */
const FOLDER_TO_SECTION_ID = {
  APPETIZERS: 'appetizers',
  BEEF: 'beef',
  'CANTONESE CHOW MEIN': 'cantonese-chow-mein',
  CHICKEN: 'chicken',
  'CHOP SUEY': 'chop-suey',
  /** Named .jpeg assets are wired in menu data, not the digit-JPG manifest. */
  'DINNER COMBO': 'dinner-combos',
  'FOO YOUNG': 'egg-foo-young',
  'FRIED RICE': 'fried-rice',
  'HOT POT': 'hot-pot',
  PORK: 'pork',
  SEAFOOD: 'seafood',
  SIDES: 'sides-drinks',
  SOUP: 'soup',
  'STEAMED RICE': 'rice-plates',
  VEGETARIAN: 'vegetarian',
  'WESTERN CM': 'western-chow-fried-rice',
}

const PRIMARY_JPG = /^\d+\.jpg$/i

function main() {
  const entries = []
  if (!fs.existsSync(photosRoot)) {
    console.warn('No public/menu-photos — writing empty manifest.')
  } else {
    for (const folder of fs.readdirSync(photosRoot, { withFileTypes: true })) {
      if (!folder.isDirectory()) continue
      const sectionFolder = folder.name
      const sectionId = FOLDER_TO_SECTION_ID[sectionFolder]
      if (!sectionId) {
        console.warn(`Unknown menu-photos folder (add to FOLDER_TO_SECTION_ID): ${sectionFolder}`)
        continue
      }
      const sub = path.join(photosRoot, sectionFolder)
      for (const name of fs.readdirSync(sub, { withFileTypes: true })) {
        if (!name.isFile() || !PRIMARY_JPG.test(name.name)) continue
        const num = path.basename(name.name, '.jpg')
        const urlPath =
          '/menu-photos/' +
          sectionFolder.split('/').map(encodeURIComponent).join('/') +
          '/' +
          encodeURIComponent(name.name)
        entries.push({ sectionId, num, urlPath })
      }
    }
  }

  entries.sort((a, b) => (a.sectionId + ':' + a.num).localeCompare(b.sectionId + ':' + b.num))

  const lines = [
    '/** Auto-generated by scripts/generate-menu-photo-manifest.mjs — do not edit by hand. */',
    'export const MENU_PHOTO_BY_KEY = /** @type {Record<string, string>} */ (',
    '  Object.freeze({',
  ]
  for (const { sectionId, num, urlPath } of entries) {
    const key = `${sectionId}:${num}`
    lines.push(`    ${JSON.stringify(key)}: ${JSON.stringify(urlPath)},`)
  }
  lines.push('  })')
  lines.push(')')
  lines.push('')

  fs.writeFileSync(outFile, lines.join('\n'), 'utf8')
  console.log(`Wrote ${entries.length} entries to ${path.relative(root, outFile)}`)
}

main()
